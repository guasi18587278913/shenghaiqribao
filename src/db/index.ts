/**
 * Connect to PostgreSQL Database (Supabase/Neon/Local PostgreSQL)
 * https://orm.drizzle.team/docs/tutorials/drizzle-with-supabase
 */
import { drizzle } from 'drizzle-orm/postgres-js';
import postgres from 'postgres';
import * as schema from './schema';

const connectionString = process.env.DATABASE_URL!;

// Configure connection pool for Supabase Session mode
// Supabase Session mode has a default pool_size of 15
const client = postgres(connectionString, {
  prepare: false,
  max: 1, // Use only 1 connection in development to avoid pool exhaustion
  idle_timeout: 20, // Close idle connections after 20 seconds
  max_lifetime: 60 * 30, // Close connections after 30 minutes
});

export const db = drizzle(client, { schema });

export async function getDb() {
  return db;
}

